int	ft_sqrt(int nb)
{
	int	nb2;

	if (nb <= 0)
		return (0);
	nb2 = 46340;
	while (nb >= 576000000)
	{
		if (nb2 * nb2 == nb)
			return (nb2);
		if (nb2 < 24000)
			return (0);
		nb2--;
	}
	nb2 = 24000;
	while (nb < 576000000)
	{
		if (nb2 * nb2 == nb)
			return (nb2);
		if (nb2 == 0)
			return (0);
		nb2--;
	}
	return (0);
}
